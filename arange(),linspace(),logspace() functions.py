import numpy as np
print('arange() function')
arr1 = np.arange(1,21,1,dtype=float)
print(arr1)
arr2 = np.arange(20,1,-3,dtype=int)
print(arr2)
print('linspace() function')
arr3 = np.linspace(1,20,10,dtype=int)
print(arr3)
arr4 = np.linspace(1,200,5,dtype=int)
print(arr4)
arr5 = np.linspace(100,1,5,dtype=int)
print(arr5)
print('logspace() function')
arr6 = np.logspace(1,10,5)
print(arr6)